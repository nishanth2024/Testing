package com.controllingstatement;

public class TaskTriangleaNested {

	public static void main(String[] args) {
		
		for(int i = 0; i<=4; i = i + 1) {
			for(int j = 4; j>=i; j =j - 1 ) {
				System.out.print(" ");	
			}
			for(int k = 1; k<=i; k = k + 1) {
				System.out.print("* ");
			}
		
		
		System.out.println();
			
		
		
		}
		
		
		
		
		
		
	}
	
	
}
