package com.controllingstatement;

public class TaskHalfTriangle {

	public static void main(String[] args) {
		int i,j;
		for( i = 1; i<=4; i=i+1) {
			System.out.println();
			for(j = 4; j>i; j=j-1) {
				System.out.print(" ");	
			}
			for(j = 1; j<=i; j=j+1) {
				System.out.print("*");
			}
			System.out.println();
			
			
			
			
			
			
			
			
			
			
			
			
			
	
		}	
		
	}	
}
